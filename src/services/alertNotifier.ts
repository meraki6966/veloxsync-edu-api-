import { pool } from '../db';
import { decryptToken } from '../utils/crypto';
import { OrgSignals } from './signalEngine';

// Minimal shape we need from OrgInsightResponse — avoids circular import
export interface AlertInsightData {
  executive_summary: string;
  org_health_score: number;
  top_at_risk: {
    employee_name: string;
    primary_signal: string;
    signal_value: number;
    recommended_action: string;
  }[];
  recommended_actions: {
    priority: string;
    action: string;
    target: string;
    rationale: string;
  }[];
}

// ─────────────────────────────────────────────────────────────────────────────
// SLACK
// ─────────────────────────────────────────────────────────────────────────────

export async function sendSlackMessage(webhookUrl: string, payload: object): Promise<void> {
  const res = await fetch(webhookUrl, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(payload),
  });
  if (!res.ok) {
    throw new Error(`Slack webhook HTTP ${res.status}: ${await res.text()}`);
  }
}

function buildSlackPayload(insight: AlertInsightData, signals: OrgSignals): object {
  const urgency      = signals.critical_count > 0 ? '🚨 Critical Alert' : '⚠️ High Priority Alert';
  const healthEmoji  = insight.org_health_score >= 75 ? '🟢' : insight.org_health_score >= 50 ? '🟡' : '🔴';
  const immediateAct = insight.recommended_actions.find(a => a.priority === 'immediate')?.action
    ?? 'Review flagged employees in VeloxSync.';

  const atRiskLines = insight.top_at_risk.slice(0, 3)
    .map(e => `• *${e.employee_name}* — ${e.primary_signal.replace(/_/g, ' ')} at ${e.signal_value}/100`)
    .join('\n');

  return {
    text: `${urgency}: EI-Core Workforce Alert`,
    blocks: [
      {
        type: 'header',
        text: { type: 'plain_text', text: `${urgency} — EI-Core Workforce Alert` },
      },
      {
        type: 'section',
        text: { type: 'mrkdwn', text: insight.executive_summary },
      },
      {
        type: 'section',
        fields: [
          { type: 'mrkdwn', text: `*${healthEmoji} Org Health*\n${insight.org_health_score}/100` },
          { type: 'mrkdwn', text: `*👥 At-Risk*\n${signals.at_risk_count} employees` },
          { type: 'mrkdwn', text: `*🔥 Avg Burnout*\n${signals.avg_burnout_probability}/100` },
          { type: 'mrkdwn', text: `*🚨 Critical*\n${signals.critical_count} employees` },
        ],
      },
      ...(insight.top_at_risk.length > 0
        ? [{
            type: 'section',
            text: { type: 'mrkdwn', text: `*Top At-Risk Employees:*\n${atRiskLines}` },
          }]
        : []),
      {
        type: 'section',
        text: { type: 'mrkdwn', text: `*Immediate Action:* ${immediateAct}` },
      },
      { type: 'divider' },
      {
        type: 'context',
        elements: [
          { type: 'mrkdwn', text: `Generated by *VeloxSync EI-Core* · ${new Date().toUTCString()}` },
        ],
      },
    ],
  };
}

export async function sendSlackOrgAlert(
  orgId: number,
  insight: AlertInsightData,
  signals: OrgSignals,
): Promise<void> {
  const row = await pool.query(
    'SELECT webhook_url FROM slack_integrations WHERE organization_id = $1',
    [orgId],
  );
  if (row.rows.length === 0) return; // no Slack connected for this org

  const webhookUrl = decryptToken(row.rows[0].webhook_url);
  await sendSlackMessage(webhookUrl, buildSlackPayload(insight, signals));
  console.log(`[AlertNotifier] Slack alert sent for org ${orgId}`);
}

